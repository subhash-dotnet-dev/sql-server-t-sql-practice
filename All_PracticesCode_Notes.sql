CREATE OR ALTER TRIGGER T7 
ON EMP44 
INSTEAD OF INSERT 
AS 
DECLARE @ENO INT,@NAME VARCHAR(10),@DNO INT , @CNT INT 
SELECT @ENO = ENO ,@NAME=ENAME,@DNO = DNO FROM INSERTED 
SELECT @CNT = COUNT(*) FROM EMP44 WHERE DNO = @DNO 
