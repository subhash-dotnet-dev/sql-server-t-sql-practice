 Types of Functions. 
 ===================
1 Scalar Valued Functions (SVF) 
2 Table Valued Functions (TVF) 
 
 1. Scalar Valued Functions (SVF). 
 ===================================
=> A function is also a named T-SQL block that accepts some input perform some 
calculation and must return a value. 
=> These functions returns one value 
=> Return type must scalar types like int , varchar , money etc. 
=> Return expression is a scalar variable 

Syntax:- 
--------
CREATE OR ALTER FUNCTION <name>(parameters) RETURNS <type> 
AS 
BEGIN 
statements 
RETURN <expr> 
END 
 
