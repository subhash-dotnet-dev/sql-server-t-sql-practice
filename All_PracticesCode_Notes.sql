successful                      successful          VALID 
failed							failed              VALID 


=> Every transaction must gurantee a property called atomocity i.e. all or none 

=> If transaction contains multiple operations then if all are successful then it must be 
   saved , if one of the operation fails then entire transaction must be cancelled. 

=> The following commands provided by SQL server to handle transaction called TCL 
   commands 
 
1 commit				==> To save transactions 
2 rollback				==> To cancel transactions 
3 save transaction		==> To cancel part of the transactions 
 
=> Every transaction has a begin point and an end point 

=> In SQL server a transactions begins implicitly with DML commands and ends 
   implicitly with commit 
 
update		=>		txn begins 
					execute update 
					execute commit 
 
delete      =>		txn begins 
					execute delete 
					execute commit 

