What are Temporary Tables? 
---------------------------
=> Temporary tables stores data temporarily. 
=> These tables exists upto the session and once session closes these tables are 
   automatically deleted. 
=> Used for storing intermediate results 

Ex :- 

Method 1 :- 
===========
CREATE TABLE #TEMP 
( 
empno INT, 
183  
