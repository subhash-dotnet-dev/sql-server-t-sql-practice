WHERE clause. 
-------------
=> Where clause is used to apply filter conditions 
=> Where clause is used to get specific rows from table 

Syntax:-
-------
Where col_name op value 
=> Where condition returns true and false 
=> OP must be any relational operator like > , < , >= , <= , = , <> or !=. 
=> If cond = true row is selected , 
=> If cond = false row is not selected 
 
