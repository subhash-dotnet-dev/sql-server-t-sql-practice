 INTEGRITY CONSTRAINTS.
 ======================


=> Integrity constraints are rules to maintain data integrity i.e data quality or data 
   consistency 
=> Used to prevent users from entering invalid data . 
=> Used to enforce rules like min bal must be 1000. 
 
Types of constraints.
----------------------
  
1. NOT NULL 
2. UNIQUE 
3. PRIMARY KEY 
4. CHECK 
5. FOREIGN KEY 
6. DEFAULT 
 
=> Above constraints can be declared in two ways 
	A. Column level 
	B. Table level 
 
A. Column level. 
-------------------
=> If constraints are declared immediately after declaring column then it is called 
   column level 

Syntax:-
--------
CREATE TABLE <tabname> 
( 
colname datatype(size) constraint , 
colname datatype(size) constraint , 
) 

--------------------------------------------------------------------------

1. NOT NULL. 
-------------
=> NOT NULL constraint does not accept null values 
=> if column declared with NOT NULL is called mandatory column.

Ex :- 
CREATE TABLE emp11 
( 
empno INT , 
ename VARCHAR(10) NOT NULL 
) 

INSERT INTO emp11 VALUES(100,NULL) => ERROR 
INSERT INTO emp11 VALUES(101,'A') 

--------------------------------------------------------------------------
 
 2. UNIQUE.
 ------------
=> Unique constraint does not accept duplicates 

Ex :- 
CREATE TABLE emp12 
( 
empno INT , 
ename VARCHAR(10) NOT NULL, 
emailid VARCHAR(20) UNIQUE 
) 

INSERT INTO emp12 VALUES(100,'A','abc@gmail.com') 
INSERT INTO emp12 VALUES(101,'B','abc@gmail.com')    =>   ERROR   
INSERT INTO emp12 VALUES(102,'C',NULL) 
INSERT INTO emp12 VALUES(103,'D',NULL)               =>   ERROR 
 
Note :-
--------
Unique constraint allows one null

How to allow multiple nulls.

