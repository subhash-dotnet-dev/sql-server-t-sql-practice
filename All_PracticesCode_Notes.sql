--------------------------------------
INSERT INTO #TEMP 
SELECT EMPNO,ENAME,SAL, 
DENSE_RANK() OVER (ORDER BY SAL DESC) AS RNK 
FROM EMP 

Display temporary table:-
-------------------------
SELECT * FROM #TEMP 
Method 2 :- 
SELECT EMPNO,ENAME,SAL, 
DENSE_RANK() OVER (ORDER BY SAL DESC) AS RNK INTO #TEMP 
FROM EMP 

=> Display top 5 employees ? 
---------------------------------
SELECT * FROM #TEMP WHERE RNK <= 5 

