CURSORS. 
========
  
=> Cursors are used to process multiple rows in T-SQL program. 
=> Using cursor we can access row-by-row from table into T-SQL program. 
=> From T-SQL prog if we submit a query to SQL server , it goes to DB and gets the 
data and copies that data into temporary memory called cursor and in prog we 
can give name to the cursor and access row-by-row from cursor and process the 
row. 
 
Follow below steps to use cursor 
---------------------------------
1. Declare cursor 
2. Open cursor 
3. Fetch records from cursor 
4. Close cursor 
5. Deallocate cursor 
 
 1. Declaring cursor. 
 ====================
 
DECLARE <name> CURSOR FOR SELECT STATEMENT 
Ex :- 
DECLARE C1 CURSOR FOR SELECT ename,sal FROM emp 

 2. Opening cursor. 
 =====================
 
OPEN <cursor-name> 
Ex :- 
OPEN C1 
1 SQL server execute the select statement 
2 Data returned by select statement copied to temporary 
3 Cursor C1 points to that temporary memory 


3. Fetching records from cursor. 
=================================
=> "FETCH" statement is used to fetch record from cursor 
 
FETCH NEXT FROM <cursor-name> INTO <variables> 
Ex :- 
FETCH NEXT FROM C1 INTO @ename,@sal 
=> A fetch statement fetches one row at a time but to process multiple rows fetch 
statement should be executed multiple times, so fetch statement should be in a 
loop. 
 
4. Closing cursor. 
====================
   
CLOSE <name> 
EX :- CLOSE C1 

5. Deallocating cursor. 
========================
Syntax:- 
--------
DEALLOCATE <cursor> 
Ex :- DEALLOCATE C1 
 
@@FETCH_STATUS 
=> It is a system variable that returns fetch status i.e. fetch is successful or not 
           0 => If fetch successful 
           -1 => If fetch unsuccessful 
=> Used for loop condition 

