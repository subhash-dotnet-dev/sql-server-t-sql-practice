=> To handle runtime errors include a block called TRY ----------- CATCH ---- Block. 
 
BEGIN TRY 
statements => Statements causes exception 
END TRY 
BEGIN CATCH 
statements => Statements handles exceptions 
END CATCH 
 
=> If any statement causes runtime error then control is transferred to catch block 
and executes the statements in catch block. 
 
Ex :- 
DECLARE @a TINYINT,@b TINYINT,@c TINYINT 
BEGIN TRY 
SET @a=100 
SET @b=20 
SET @c = @a/@b 
PRINT @C 
END TRY 
BEGIN CATCH 
PRINT 'ERROR' 
END CATCH 
 
