DATABASE TRANSACTIONS. 
========================


=> A transaction is a unit of work that contains one or more dmls and must be saved 
   as a whole or must be cancelled as a whole. 
 
Ex :- money transfer 
 
acct1-------------------- 1000 --------------------------  acct2 

