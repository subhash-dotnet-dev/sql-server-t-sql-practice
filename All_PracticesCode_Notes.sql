3. PRIMARY KEY. 
---------------
=> Primary does not accept duplicates and nulls. 
=> Primary key is the combination of unique & not null 

   Primary key = unique + not null
   
=> In tables one column must be there to uniquely identify the records and into that 
   column duplicates and nulls are not allowed , so declare that column with primary key.
   
Ex :- 
CREATE TABLE emp13 
