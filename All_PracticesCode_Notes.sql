CREATE OR ALTER PROCEDURE TRANSFER 
@from_acno INT, 
@to_acno INT, 
@amt MONEY, 
@status VARCHAR(50) OUTPUT 
AS 
BEGIN 
DECLARE @from_bal MONEY, @to_bal MONEY 
SELECT @from_bal = bal FROM accounts WHERE accno = @from_acno 
SELECT @to_bal = bal FROM accounts WHERE accno = @to_acno 
IF @amt > @from_bal 
BEGIN 
SET @status = 'Insufficient balance' 
RAISERROR('Insufficient balance', 16, 1) 
RETURN 
END 
UPDATE accounts SET bal = bal - @amt WHERE accno = @from_acno 
UPDATE accounts SET bal = bal + @amt WHERE accno = @to_acno 
INSERT INTO transactions (ttype, tdate, tamt, accno) 
VALUES ('T-', GETDATE(), @amt, @from_acno) -- Sender transaction 
INSERT INTO transactions (ttype, tdate, tamt, accno) 
VALUES ('T+', GETDATE(), @amt, @to_acno) -- Receiver transaction 
SET @status = 'Transfer successful' 
END 

 USER DEFINE FUNCTIONS. 
