=> With the help of proc & func a big T-SQL program can be divided into small 
modules. 
 
2. Reusability :-
-----------------
=> Because these programs are created with name , so we can call these programs 
from another program. 

3. Invoked from front-end applications :- 
-----------------------------------------
=> Because proc & func are stored in DB , so front-end applications which are 
connected to DB can invoke these programs.

4. Improves performance :- 
-----------------------------
=> Proc & Func are precompiled i.e. when we create proc/func program is compiled 
and stored in DB and whenever we call proc/func only execution is repeated but 
not compilation , so this improves performance. 
 
 STORED PROCEDURES. 
 ====================
=> A stored procedure is a named T-SQL block that accepts one or more values and 
performs some action on DB and may or may not returns a value. 
=> Procedures are created to perform one or more actions on DB like insert , update , 
delete , select etc. 

SYNTAX:- 
--------
CREATE OR ALTER PROCEDURE <name> 
parameters if any 
AS 
statements 
 
