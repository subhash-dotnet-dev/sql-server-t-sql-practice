 SUB-QUERIES / NESTED QUERIES. 
 ==============================

=> A query in another query is called sub-query or nested query 
=> One query is called inner / sub / child query 
=> Other query is called outer / main / parent query 
=> First SQL server executes inner query then it executes outer query 
=> Output of inner query is input to outer query 
=> Use sub-query when where condition based on unknown value 

-----------------------------------------------------------------
 
Types of sub-queries. 
----------------------
1. Non co-related subqueries / Single row sub-queries 
2. Multi row sub-queries 
3. Co-related subqueries 
4. Derived Tables / CTEs 
5. Scalar sub-queries 


1. Non co-related sub-queries / Single row sub-queries . 
-------------------------------------------------------
=> If inner query returns one value then it is called single row sub-query 

  
Syntax:- 
--------
SELECT columns 
FROM tabname 
WHERE colname OP (SELECT statement) 
 
=> Op can be any relational operator like = > >= < <= <> 

