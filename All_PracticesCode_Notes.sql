DROP TABLE @tname (Dynamic SQL Command) 

=> Dynamic SQL is useful when we do not know table names and column names until 
	runtime. 

=> Dynamic SQL commands are executed by using EXEC procedure EXEC (' dynamic SQL command ') 
=> Dynamic SQL command that you want to execute should be passed as a string to 
	EXEC procedure. 

